// Función para obtener un producto por su slug
export function getProductBySlug(slug: string) {
  return products.find((product) => product.slug === slug)
}

// Función para obtener productos relacionados (excluyendo el producto actual)
export function getRelatedProducts(currentProductId: number, limit = 3) {
  return products.filter((product) => product.id !== currentProductId).slice(0, limit)
}

